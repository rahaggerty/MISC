%DY_HILL functional equations for the change in Y

dY = a(1).*((...
(((x.^n(1)./(k(1).^n(1)+x.^n(1))).^(g(1)^2.*((g(1)+1)/2)).*((k(1).^n(1)+x.^n(1))/k(1).^n(1)).^(g(1)^2*((g(1)-1)/2)))./...
    ((1./(k(1).^n(1)+1)).^(g(1)^2.*((g(1)+1)/2)).*((k(1).^n(1)+1)/k(1).^n(1)).^(g(1)^2*((g(1)-1)/2)))).*... %x--y
(((y.^n(2)./(k(2).^n(2)+y.^n(2))).^(g(2)^2.*((g(2)+1)/2)).*((k(2).^n(2)+y.^n(2))/k(2).^n(2)).^(g(2)^2*((g(2)-1)/2)))./...
    ((1./(k(2).^n(2)+1)).^(g(2)^2.*((g(2)+1)/2)).*((k(2).^n(2)+1)/k(2).^n(2)).^(g(2)^2*((g(2)-1)/2)))).*... %y--y
(((z.^n(3)./(k(3).^n(3)+z.^n(3))).^(g(3)^2.*((g(3)+1)/2)).*((k(3).^n(3)+z.^n(3))/k(3).^n(3)).^(g(3)^2*((g(3)-1)/2)))./...
    ((1./(k(3).^n(3)+1)).^(g(3)^2.*((g(3)+1)/2)).*((k(3).^n(3)+1)/k(3).^n(3)).^(g(3)^2*((g(3)-1)/2)))))... %z--y
-y);

end
